from math import trunc, floor

n = float(input("Digite um número: "))
print("O número",n)
print("tem a parte inteira",floor(n))

# Outra forma de fazer é:
n = float(input("Digite um número: "))
print("O número",n)
print("tem a parte inteira",trunc(n))

# Outra forma de fazer é:
n = float(input("Digite um número: "))
